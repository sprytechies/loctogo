    <!-- script for show pop up on login link -->
    <script>
                   function login(){
                    $.ajax({
                        type:'POST',
                        url:'index.php?r=site/login',
                        success: function(data)
                        {
                           $('#myModal').html(data);
                           $('#myModal').modal();
                        }
                    });
                   return false;
                   }
              </script>
